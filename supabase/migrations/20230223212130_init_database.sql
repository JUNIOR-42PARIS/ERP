create table "public"."clients" (
    "nom" text not null
        constraint longueur_nom
        CHECK (char_length(nom) > 0 and char_length(nom) <= 50),
    "adresse" text not null
        constraint longueur_adresse
        CHECK (char_length(adresse) > 0 and char_length(adresse) <= 250),
    "siret" text
        constraint longueur_siret
        CHECK (siret IS NULL OR char_length(siret) = 0 OR char_length(siret) = 9),
    "numero_tva" text 
        constraint longueur_numero_tva
        CHECK (numero_tva IS NULL OR char_length(numero_tva) = 0 AND char_length(numero_tva) = 13),
    "created_at" timestamp with time zone not null default now()
);


alter table "public"."clients" enable row level security;

create type "public"."mission_status" as enum (
    'prospect',
    'mission',
    'abandonnee',
    'finie'
);
create table "public"."missions" (
    "id" bigint generated by default as identity not null,
    "nom" text not null unique constraint nom_mission_longueur check (char_length(nom) > 0),
    "status" mission_status not null default 'prospect'::mission_status,
    "client" text not null,
    "description" text,
    "lien_github" text,
    "lien_drive" text,
    "nom_intermediaire" text,
    "email_intermediaire" text constraint email_intermediaire_longueur check (char_length(email_intermediaire) > 0),
    "telephone_intermediaire" text constraint telephone_intermediaire_longueur  check (char_length(telephone_intermediaire) > 0),
    "created_by" uuid not null,
    "created_at" timestamp with time zone default now()
);


alter table "public"."missions" enable row level security;

create table "public"."missions_intervenants" (
